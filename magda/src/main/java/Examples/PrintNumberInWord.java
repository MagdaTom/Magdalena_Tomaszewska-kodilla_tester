package Examples;

public class PrintNumberInWord {
    public static void main(String[] args) {

        int number = 7;

        switch(number){
            case 1:
                System.out.println("One");
                break;
            case 2:
                System.out.println("Two");
                break;
            case 3:
                System.out.println("Three");
                break;
            default:
                System.out.println("Number not found");

        }
    }
}
